package final_calc;
import final_calc.*;
 class Ez_Exeption  extends Exception{

     public Ez_Exeption() {
     }

     public Ez_Exeption(String message) {
         super(message);
     }

     public Ez_Exeption(String message, Throwable cause) {
         super(message, cause);
     }

     public Ez_Exeption(Throwable cause) {
         super(cause);
     }

     public Ez_Exeption(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
         super(message, cause, enableSuppression, writableStackTrace);
     }
 }

